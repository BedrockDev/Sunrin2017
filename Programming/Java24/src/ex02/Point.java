package ex02;

public class Point {
	
	private int x, y;

	public Point(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	@Override
	public String toString() {
		return "Point [x=" + x + ", y=" + y + "]";
	}
	
	// this all methods were created automatically 
	// from Source > Generate Constructor / Generate toString
}
