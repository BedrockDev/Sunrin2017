
public class Exam {

	public static void main(String[] args) {
		System.out.println("3 + 4"); // the entire string "3 + 4"
		System.out.println(3 + 4); // calculated value of 3 + 4 = 7
		System.out.println(3 + 4 + "4"); // calculated value of 3 + 4 = 7 and string "4" merged
		System.out.println("3" + 3 + 4); // string "3" and the two following operands converted to seperated string "3" and "4" merged
	}

}
